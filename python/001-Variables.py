#!/usr/bin/env python3

# variable = a reusable container for storing a value
#                   a variable behaves as if it were the value it contains

# INTEGER
age = 21
players = 2
quantity = 5
print("Integers")
print(f"You are {age} years old")
print(f"There are {players} players online")
print(f"You would like to buy {quantity} items\n")

# FLOAT
gpa = 3.2
distance = 2.5
price = 10.99
print("Floats")
print(f"Your gpa is {gpa}")
print(f"You ran {distance}Km")
print(f"The price is ${price}\n")

# STRING
name = "Bro"
food = "pizza"
email = "Bro123@gmail.com"
print("Strings")
print(f"Hello {name}")
print(f"You like {food}")
print(f"Your email is: {email}\n")

# BOOLEAN
online = True
for_sale = False
running = False
print("Booleans")
print(f"Are you online?: {online}")
print(f"Is the item for sale?: {for_sale}")
print(f"Game running: {running}\n")
