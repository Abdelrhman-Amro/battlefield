#!/usr/bin/env python3

# By default the result of input() is a string.
age = input("What is your age? ")
print(type(age), '\n')

# You can convert it to an integer by using the int() function.
# Use type casting BRO🤷‍♂️🤷‍♂️🤷‍♂️
age = int(input("What is your age? (NOW IT's differen sorrey): "))
print(type(age))
